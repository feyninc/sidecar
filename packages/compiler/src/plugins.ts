/** Plugin package generation for Claude desktop hosts. */
import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { loadProjectIdentity } from "./identity.js";
import { emitClaudeAgents } from "./plugin-components/agents.js";
import { copyCommands } from "./plugin-components/commands.js";
import { copyHooks } from "./plugin-components/hooks.js";
import { copyClaudePassthroughDirectories } from "./plugin-components/passthrough.js";
import { copySkills } from "./plugin-components/skills.js";
import type { ProjectIdentity, SidecarManifest } from "./types.js";
import { stripUndefined } from "./utils.js";

/** Emits all plugin package targets for the current MCP manifest. */
export async function buildPluginPackages(
  rootDir: string,
  outRoot: string,
  manifest: SidecarManifest,
): Promise<void> {
  const identity = await loadProjectIdentity(rootDir);
  await buildClaudePlugin(rootDir, outRoot, identity, manifest);
}

/** Emits a Claude/Cowork plugin package that references the hosted MCP server. */
async function buildClaudePlugin(
  rootDir: string,
  outRoot: string,
  identity: ProjectIdentity,
  manifest: SidecarManifest,
): Promise<void> {
  const pluginDir = path.join(outRoot, "claude-plugin");
  await mkdir(path.join(pluginDir, ".claude-plugin"), { recursive: true });
  await copySkills(rootDir, path.join(pluginDir, "skills"));
  await copyCommands(rootDir, path.join(pluginDir, "commands"));
  await copyHooks(rootDir, path.join(pluginDir, "hooks"));
  await emitClaudeAgents(rootDir, path.join(pluginDir, "agents"));
  await copyClaudePassthroughDirectories(rootDir, pluginDir);

  await writeJson(path.join(pluginDir, ".claude-plugin", "plugin.json"), {
    name: identity.slug,
    version: identity.version,
    description: identity.description,
    displayName: identity.name,
    installationPreference: "available",
  });

  await writeJson(path.join(pluginDir, "version.json"), {
    version: identity.version,
    generatedBy: "sidecar",
  });

  await writeJson(path.join(pluginDir, ".mcp.json"), {
    mcpServers: {
      [identity.slug]: {
        type: "http",
        url: "${SIDECAR_MCP_URL}",
      },
    },
  });

  await writeJson(path.join(pluginDir, "settings.json"), {
    sidecar: {
      tools: manifest.tools.map((entry) => entry.id),
    },
  });

  await writeFile(
    path.join(pluginDir, "README.md"),
    `# ${identity.name} Claude Plugin

This package was generated by Sidecar.

Set \`SIDECAR_MCP_URL\` to the hosted MCP endpoint for this app.

For local testing, run \`sidecar dev --tunnel\` and use the printed HTTPS MCP URL.

Claude/Cowork plugin-specific components such as skills, slash commands, agents, hooks, output styles, themes, monitors, assets, and bin scripts will be emitted here when the source project defines them.
`,
  );
}

/** Writes formatted JSON while dropping undefined fields. */
async function writeJson(
  filePath: string,
  value: Record<string, unknown>,
): Promise<void> {
  await mkdir(path.dirname(filePath), { recursive: true });
  await writeFile(
    filePath,
    `${JSON.stringify(stripUndefined(value), null, 2)}\n`,
  );
}
